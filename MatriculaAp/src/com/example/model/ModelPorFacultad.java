package com.example.model;


public class ModelPorFacultad {

	private String salud;
	private String empresariales;
	private String educacion;
	private String ingenieria;
	
	
	public String getSalud() {
		return salud;
	}
	public void setSalud(String salud) {
		this.salud = salud;
	}
	public String getEmpresariales() {
		return empresariales;
	}
	public void setEmpresariales(String empresariales) {
		this.empresariales = empresariales;
	}
	public String getEducacion() {
		return educacion;
	}
	public void setEducacion(String educacion) {
		this.educacion = educacion;
	}
	public String getIngenieria() {
		return ingenieria;
	}
	public void setIngenieria(String ingenieria) {
		this.ingenieria = ingenieria;
	}
	
}
