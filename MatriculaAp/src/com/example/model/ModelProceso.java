package com.example.model;

public class ModelProceso {
	
	private String virtualProceso;
	private String virtualConfirm;
	private String precensialProceso;
	private String presencialConfirm;
	
	
	public String getVirtualProceso() {
		return virtualProceso;
	}
	public void setVirtualProceso(String virtualProceso) {
		this.virtualProceso = virtualProceso;
	}
	public String getVirtualConfirm() {
		return virtualConfirm;
	}
	public void setVirtualConfirm(String virtualConfirm) {
		this.virtualConfirm = virtualConfirm;
	}
	public String getPrecensialProceso() {
		return precensialProceso;
	}
	public void setPrecensialProceso(String precensialProceso) {
		this.precensialProceso = precensialProceso;
	}
	public String getPresencialConfirm() {
		return presencialConfirm;
	}
	public void setPresencialConfirm(String presencialConfirm) {
		this.presencialConfirm = presencialConfirm;
	}

	
}
